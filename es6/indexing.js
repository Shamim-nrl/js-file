
// map 

const numbers =[10,20,30,40,50]
numbers.map((item,)=>{
    console.log("item: ", item);
})


const numbers =[10,20,30,40,50]
numbers.map((item, index,)=>{
    console.log("item: ", item, "index: ", index);
})


const numbers =[10,20,30,40,50]
numbers.map((item, index, arr)=>{
    console.log("item: ", item, "index: ", index, "arr: ", arr);
})